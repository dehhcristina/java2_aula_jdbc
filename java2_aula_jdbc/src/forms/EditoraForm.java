    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
        int selectd = tabela.getSelectedRow();
        EditoraID.setText(tabela.getValueAt(selectd, 0).toString());
        Nome.setText(tabela.getValueAt(selectd, 1).toString());
        Municipio.setText(tabela.getValueAt(selectd, 2).toString());
        this.mode = "UPD";
    }//GEN-LAST:event_tabelaMouseClicked

    private void BotaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoRemoverActionPerformed
        int opt = JOptionPane.showConfirmDialog(this, "Excluir registro?");

        if (opt == JOptionPane.YES_OPTION) {
            Editora editora = new Editora();
            editora.setEditora_id(Integer.parseInt(EditoraID.getText()));

            try {
                editoraDAO.delete(editora);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }

            EditoraID.setText("");
            Nome.setText("");
            Municipio.setText("");
            this.mode = "INS";

            Listar();
        }
    }//GEN-LAST:event_BotaoRemoverActionPerformed

    private void BotaoNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoNovoActionPerformed
        EditoraID.setText("");
        Nome.setText("");
        Municipio.setText("");
        this.mode = "INS";
    }//GEN-LAST:event_BotaoNovoActionPerformed

    public void Listar() {
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        //limpar tabela
        model.setNumRows(0);
        try {
            //busca lista de objetos
            for (Editora editora : editoraDAO.findAll()) {
                String linha[] = {"" + editora.getEditora_id(), editora.getNome(), editora.getMunicipio()};
                model.addRow(linha);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

